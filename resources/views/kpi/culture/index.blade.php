    <script>
        $.ajaxSetup({
            headers: {
                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
            }
        });
        var table = $('#datatables').DataTable({
            processing: true,
            serverSide: true,
            ajax: "{{ route('kpi.culture') }}",
            columns: [
                {
                    data: 'id',
                    name: 'id'
                },
                {
                    data: 'culture',
                    name: 'culture'
                },
                {
                    data: 'indikator',
                    name: 'indikator'
                },
                {
                    data: 'action',
                    name: 'action',
                    orderable: false,
                    searchable: false
                },
            ],
            columnDefs: [
